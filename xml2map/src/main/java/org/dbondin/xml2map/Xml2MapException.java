package org.dbondin.xml2map;

public class Xml2MapException extends Exception {

	private static final long serialVersionUID = -3229473496839883061L;

	public Xml2MapException() {
		super();
	}

	public Xml2MapException(String message) {
		super(message);
	}

	public Xml2MapException(Throwable cause) {
		super(cause);
	}

	public Xml2MapException(String message, Throwable cause) {
		super(message, cause);
	}

	public Xml2MapException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
